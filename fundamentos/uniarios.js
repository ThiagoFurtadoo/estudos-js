//Note: Incrementing the value of 'num1' using the postfix increment operator (++).
let num1 = 1
let num2 = 2

num1+++
console.log(num1)

//Note: Decrementing the value of 'num1' using the prefix decrement operator (--).
--num1
console.log(num2)

//Note: Using prefix increment operator (++) and postfix decrement operator (--) in a comparison expression.
//Note: Prefix increment operator increases 'num1' by 1 before the comparison, while postfix decrement operator decreases 'num2' by 1 after the comparison.
console.log(++num1 == num2--)

//Note: Comparing the current values of 'num1' and 'num2' after the operations.
console.log(num1 == num2)
